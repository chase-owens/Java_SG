public class House3D {
    private int numberOfStories;
    private int squareFoot;
    private boolean useBrick;
    private String mainColor;
    private String sidingColor;
    private String doorColor;

    public int getNumberOfStories() {
        return numberOfStories;
    }

    public void setNumberOfStories(int numberOfStories) {
        this.numberOfStories = numberOfStories;
    }

    public int getSquareFoot() {
        return squareFoot;
    }

    public void setSquareFoot(int squareFoot) {
        this.squareFoot = squareFoot;
    }

    public boolean isUseBrick() {
        return useBrick;
    }

    public void setUseBrick(boolean useBrick) {
        this.useBrick = useBrick;
    }

    public String getMainColor() {
        return mainColor;
    }

    public void setMainColor(String mainColor) {
        this.mainColor = mainColor;
    }

    public String getSidingColor() {
        return sidingColor;
    }

    public void setSidingColor(String sidingColor) {
        this.sidingColor = sidingColor;
    }

    public String getDoorColor() {
        return doorColor;
    }

    public void setDoorColor(String doorColor) {
        this.doorColor = doorColor;
    }
    
    
}
